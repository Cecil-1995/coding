<?php

class Solution
{

    /**
     * @param String[] $words
     * @return String[]
     */
    function maxRectangle($words)
    {
        $mapLen    = [];
        $map       = [];
        $maxLength = 0;
        $pre       = [];
        foreach ($words as $word) {
            $mapLen[strlen($word)][] = $word;
            $map[$word]              = true;
            $maxLength               = max($maxLength, strlen($word));
            $tempPre                 = &$pre;
            for ($i = 0; $i < strlen($word); ++$i) {
                if (!isset($tempPre[$word[$i]])) {
                    $tempPre[$word[$i]] = [];
                }
                $tempPre = &$tempPre[$word[$i]];
            }
        }

        $ans = [];
        $max = 0;
        foreach ($mapLen as $len => $items) {
            $this->helper($items, [], $len, $map, $ans, $max, $maxLength, $pre);
        }

        return $ans;
    }

    function helper($words, $current, $len, $map, &$ans, &$max, $maxLength, $pre)
    {
        if (count($current) > $maxLength || $maxLength * $len <= $max) {
            return null;
        }
        // 判断当前current是否满足条件
        $flag = true;
        for ($i = 0; $i < $len; ++$i) {
            $s       = '';
            $tempPre = $pre;
            foreach ($current as $item) {
                $s .= $item[$i];
                if (!isset($tempPre[$item[$i]])) {
                    return null;
                }
                $tempPre = $tempPre[$item[$i]];
            }
            if ($s !== '' && !isset($map[$s])) {
                $flag = false;
                break;
            }
        }

        // 更新结果
        if ($flag && $len * count($current) > $max) {
            $max = $len * count($current);
            $ans = $current;
        }

        foreach ($words as $word) {
            $current[] = $word;
            $this->helper($words, $current, $len, $map, $ans, $max, $maxLength, $pre);
            array_pop($current);
        }
    }
}

